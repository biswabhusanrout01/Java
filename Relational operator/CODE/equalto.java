public class equalto{
    public static void main(String[] args) {
        int a = 10;
        int b = 15;

        if( a > b ){
            System.out.println( a +  " is greater than "  + b);
        }
        else if ( a < b ){
            System.out.println( b +  " is greater than "  + a );
        }
        else {
            System.out.println("both are equal");
        }
    }

}